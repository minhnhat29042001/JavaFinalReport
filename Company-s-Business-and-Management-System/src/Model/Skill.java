public class Skill {

    public Skill( String name) {

        this.name = name;
    }

    private String name;

    Skill() {
 
    }

    

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return name ;
    }
    
}
