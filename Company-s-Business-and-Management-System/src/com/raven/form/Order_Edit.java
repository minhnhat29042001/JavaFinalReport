    public void showDH(){
        Order order = Order_Modify.getOrderDH();
        String dh = String.valueOf(order.getOrder_DH());
        String num = dh.substring(2);
        int num1 = Integer.valueOf(num);
        num1+=1;
        text_Order.setText("OD"+num1);
        text_Date.setText(java.time.LocalDate.now()+"");
    }
    
    public void caltotal(int mon){
        totaltotal=totaltotal+mon;
    }
    
    public void showOrder(){
        String orderdh = text_Order.getText();
       List<Order> orderList = Order_Modify.findByName(orderdh);
       
        tableModel.setRowCount(0);
        orderList.forEach((order) -> {
            Item item = Item_Modify.getItemId(order.getItem_ID());
            int price = item.getPrice();
            int num = order.getNumber();
            int total = price*num;
            tableModel.addRow(new Object[]{item.getItem_Name(),order.getNumber(),total});
        });
        
    }
    
    
    
    
    public void showProductCombobox(){
        
       List<Item> itemList = Item_Modify.findAll();
      
       for(Item item :itemList){
           product_cbb.addItem(item.getItem_Name());
        }
    }
    
    
    
    private void Search(){
        SearchTextField.addEvent(new EventTextField() {
            @Override
            public void onPressed(EventCallBack call) {
               String input = SearchTextField.getText();
               
        if (input != null && input.length() > 0){
            
            
            customerList = Customer_Modify.findByPhone(input);
            for(Customer customer :customerList){
                text_Name.setText(customer.getCustomerName());
            text_Phone.setText(customer.getCustomerPhone());
            cusID = customer.getCustomer_ID();
        }
        }  
       

            }

            @Override
            public void onCancel() {
                SearchTextField.setText("");
            }
        });
    }
    
