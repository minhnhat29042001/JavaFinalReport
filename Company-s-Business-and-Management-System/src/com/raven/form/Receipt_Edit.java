    public void showReceipt() {
        List<Receipt> receiptList = Receipt_Modify.findAll();
        tableModel.setRowCount(0);
        receiptList.forEach((rec) -> {
            tableModel.addRow(new Object[]{rec.getReceipt_ID(),rec.getDateReceipt(),rec.getDatePrintReceipt(),rec.getOrder_ID(),rec.getEmployee_ID()});
        });
    }

    public void showOrderCombobox(){
       List<Order> orderList = Order_Modify.findAll();
       
       for(Order order : orderList){
           combobox_Order.addItem((order.getOrder_ID()+""));
        }
    }
    
    public void showEmployeeCombobox(){
       List<Staff> staffList = Staff_Modify.findAll();
       
       for(Staff staff : staffList){
           combobox_Employee.addItem((staff.getId()+""));
        }
    }
    
