    private void button_FixActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_FixActionPerformed
        int row = table1.getSelectedRow();
        button_Insert.setVisible(false);
        int receiptId= Integer.valueOf(String.valueOf(table1.getValueAt(row, 0))); // lấy đc id
        Receipt receipt = Receipt_Modify.getReceiptbyId(receiptId);

        Text_ReceiptID.setText(String.valueOf(receipt.getReceipt_ID()));
        Text_DatePrint.setText(String.valueOf(receipt.getDatePrintReceipt()));
        Text_Date.setText(String.valueOf(receipt.getDateReceipt()));
        
        
        
    }//GEN-LAST:event_button_FixActionPerformed

    private void button_SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_SubmitActionPerformed
        int Id = Integer.valueOf(Text_ReceiptID.getText());
        int order = Integer.valueOf(combobox_Order.getSelectedItem().toString());
        int employee = Integer.valueOf(combobox_Employee.getSelectedItem().toString());
        
        String date = Text_Date.getText();
        String dateprint = Text_DatePrint.getText();

        Receipt receipt = new Receipt(Id,date, dateprint,order, employee);
        
        
        Receipt_Modify.update(receipt);
        showReceipt();
        
        Text_ReceiptID.setText("");
        Text_Date.setText("");
        Text_DatePrint.setText("");
    }//GEN-LAST:event_button_SubmitActionPerformed

    private void button_InsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_InsertActionPerformed
        int order = Integer.valueOf(combobox_Order.getSelectedItem().toString());
        int employee = Integer.valueOf(combobox_Employee.getSelectedItem().toString());
        
        String date = Text_Date.getText();
        String dateprint = Text_DatePrint.getText();

        Receipt receipt = new Receipt(date, dateprint,order, employee);
        Receipt_Modify.insert(receipt);
        showReceipt();
        
        Text_ReceiptID.setText("");
        Text_Date.setText("");
        Text_DatePrint.setText("");
        
    }//GEN-LAST:event_button_InsertActionPerformed

